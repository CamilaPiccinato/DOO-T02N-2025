public class Aula5 {

    public static void main(String[] args) {

        imparOupar();

    }

    public static void imparOupar(){
        int numero = 8;
        if (numero % 2 == 0) {
            System.out.println(numero + " é par.");
        } else {
            System.out.println(numero + " é ímpar.");
        }
    }


}
